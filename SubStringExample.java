package javaString;

public class SubStringExample {    
	 public static void main(String args[]){    
		 String s="MorganStanley";    
		 System.out.println("Original String: " + s);  
		 System.out.println("Substring starting from index 6: " +s.substring(6));//Stanley    
		 System.out.println("Substring starting from index 0 to 6: "+s.substring(0,6)); //Morgan  
		 }  
		}   
